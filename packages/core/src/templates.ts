import type { Spec, Task, Requirement, SpecMetadata } from './types.js';

/**
 * Default spec template
 */
export const DEFAULT_SPEC_TEMPLATE = `# {title}

## Overview

{description}

## Status

**Status:** {status}

## Requirements

{requirements}

## Design

{design}

## Tasks

{tasks}

## Notes

{notes}
`;

/**
 * Create a new spec from template
 */
export function createSpecFromTemplate(
  id: string,
  title: string,
  description: string,
  template: string = DEFAULT_SPEC_TEMPLATE
): Spec {
  const now = new Date();
  
  return {
    id,
    title,
    description,
    status: 'draft',
    createdAt: now,
    updatedAt: now,
    path: `./specs/${id}`,
    requirements: [],
    tasks: [],
  };
}

/**
 * Generate markdown from spec
 */
export function specToMarkdown(spec: Spec): string {
  const lines: string[] = [
    `# ${spec.title}`,
    '',
    `## Overview`,
    '',
    spec.description,
    '',
    `## Status`,
    '',
    `**Status:** ${spec.status}`,
    '',
  ];
  
  if (spec.requirements && spec.requirements.length > 0) {
    lines.push(`## Requirements`, '', ...spec.requirements.map(r => `- ${r.description}`), '');
  }
  
  if (spec.tasks && spec.tasks.length > 0) {
    lines.push(`## Tasks`, '', ...spec.tasks.map(t => `- [${t.status === 'done' ? 'x' : ' '}] ${t.description}`), '');
  }
  
  if (spec.metadata?.notes) {
    lines.push(`## Notes`, '', spec.metadata.notes, '');
  }
  
  return lines.join('\n');
}

