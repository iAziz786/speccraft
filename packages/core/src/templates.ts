/**
 * Generate tasks template
 */
export function generateTasksTemplate(spec: Spec): string {
  const lines: string[] = [
    `# Tasks: ${spec.title}`,
    '',
    '## Implementation Checklist',
    '',
  ];
  
  // Group tasks by requirement
  const requirementTasks = new Map<string, Task[]>();
  const uncategorizedTasks: Task[] = [];
  
  for (const task of spec.tasks || []) {
    if (task.requirementIds && task.requirementIds.length > 0) {
      for (const reqId of task.requirementIds) {
        if (!requirementTasks.has(reqId)) {
          requirementTasks.set(reqId, []);
        }
        requirementTasks.get(reqId)!.push(task);
      }
    } else {
      uncategorizedTasks.push(task);
    }
  }
  
  // Output tasks grouped by requirement
  for (const [reqId, tasks] of requirementTasks) {
    const requirement = spec.requirements?.find(r => r.id === reqId);
    lines.push(`### ${requirement?.description || reqId}`, '');
    for (const task of tasks) {
      lines.push(`- [${task.status === 'done' ? 'x' : ' '}] ${task.description}`);
    }
    lines.push('');
  }
  
  // Uncategorized tasks
  if (uncategorizedTasks.length > 0) {
    lines.push('### General', '');
    for (const task of uncategorizedTasks) {
      lines.push(`- [${task.status === 'done' ? 'x' : ' '}] ${task.description}`);
    }
    lines.push('');
  }
  
  lines.push('---', '', '*Generated by SpecVibe*');
  
  return lines.join('\n');
}

/**
 * Create a requirement
 */
export function createRequirement(
  description: string,
  type: Requirement['type'] = 'functional',
  priority: Requirement['priority'] = 'must'
): Requirement {
  return {
    id: `req-${Date.now()}`,
    description,
    type,
    priority,
    satisfied: false,
  };
}

/**
 * Create a task
 */
export function createTask(
  description: string,
  requirementIds?: string[],
  estimate?: string
): Task {
  return {
    id: `task-${Date.now()}`,
    description,
    status: 'todo',
    requirementIds,
    estimate,
  };
}
